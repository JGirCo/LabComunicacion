const byte* getCharBitmap(char c) {
  if (c >= 'A' && c <= 'Z') {
    return alphabet[c - 'A'];
  } else if (c >= '0' && c <= '9') {
    return alphabet[c - '0' + 26];
  } else if (c == '.') {
    return alphabet[36];
  } else if (c == ':') {
    return alphabet[37];
  } else {
    // Fallback to checkboard pattern
    static const byte checkboard[8] = {
      B10101010,
      B01010101,
      B10101010,
      B01010101,
      B10101010,
      B01010101,
      B10101010,
      B01010101
    };
    return checkboard;
  }
}

